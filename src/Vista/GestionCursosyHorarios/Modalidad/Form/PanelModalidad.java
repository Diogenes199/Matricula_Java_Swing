    public void dataLoad(){
        if(modalidad != null){
            txtId.setText(modalidad.getId());
            txtModalidad.setText(modalidad.getDescripcion());
        }else{
            txtId.setText("");
            txtModalidad.setText("");
        }
    }
    
    public void dataSave(){
        if(modalidad == null){
            modalidad = new Modalidad();
        }if(
                !txtId.getText().trim().isEmpty() &&
                !txtModalidad.getText().trim().isEmpty() 
                ){
           
            modalidad.setId(txtId.getText().trim());
            modalidad.setDescripcion(txtModalidad.getText().trim());
        }else{
            modalidad = null;
        }
            
    }
   
